package com.intheeast.Operators;

public class ShiftExample {

	public static void main (String[] args) {
		
		int a = 5, b = 10, c = 0;
		// LSB : b[0] 
		// MSB : b[31]
		// a : ob0000_0000_0000_0000_0000_0000_0000_0101
// shiftLeft : ob0000_0000_0000_0000_0000_0000_0000_1010	
		
		// b[0]  :  0 
		// b[1] <- b[0]
		// b[2] <- b[1]
		// b[3] <- b[2]
		// b[4] <- b[3]
		// b[5] <- b[4]
		//     ...
		// 버린다 <- b[31]
		int shiftLeft = a << 1; //bit가 왼쪽으로 한칸씩 이동 
	 	
		// a : ob0000_0000_0000_0000_0000_0000_0000_0101
// shiftRight: ob0000_0000_0000_0000_0000_0000_0000_0010
		int shiftRight = a >> 1;
		
		// 8      : 0x0000_0000_0000_0000_0000_0000_0000_1000
		// 1의 보수 : 0x1111_1111_1111_1111_1111_1111_1111_0111		
		// +1     
		// -8     : 0x1111_1111_1111_1111_1111_1111_1111_1000
		int unsignedShift = -8 >>> 1;
		// >>> : 부호 없는 비트 오른쪽 이동 연산자
		// 0x1111_1111_1111_1111_1111_1111_1111_1000
		// >>> 1
		// 0x0111_1111_1111_1111_1111_1111_1111_1100 -> 2147483644
		
		
		return;
	}
}
